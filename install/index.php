<h1>Übersicht (Devbird v0.4.0)</h1>
<h2>Was ist Devbird?</h2>
<p>Devbird ist ein in PHP geschriebenes, vollwertiges, aber schlankes Blogsystem.</p>
<h2>Für wen ist Devbird geeignet?</h2>
<p>Devbird ist ein vollwertiges Blogsystem.</p>
<p>Trotzdem ist es recht schlank. Aber es fehlen auch bekannte Features wie ein <a href="http://de.wikipedia.org/wiki/Wysiwyg">WYSIWYG</a>-Editor (Trackbacks sind ja jetzt doch implementiert).</p>
<p>Deshalb ist Devbird gerade für die Leute gedacht, die auf diesen Schnickschnack verzichten können. Obwohl ich versucht habe, die Installation und Nutzung so einfach wie möglich zu halten, muss hier und da trotzdem noch ein bisschen Hand angelegt werden. Auch deshalb ist es eher was für die erfahrenen User.</p>
<h2>Wer hat Devbird geschrieben?</h2>
<p>Ich, auch bekannt als BadBoy_. Mein richtiger Name ist Jan-Erik und ich bin 17 Jahre alt.</p>
<p>Nachdem ich 2007 mein erstes Blogsystem zusammengeschustert habe, kam mir die Idee das ganze besser aufzuteilen.<br />
Ich wollte Code und Design strikter trennen und so die Pflege des Systems vereinfachen.<br />
Daraus entstanden ist dann Devbird.<br />
Wirklich sauber ist aber auch das nicht mehr, Devbird wird nicht mehr so fleißig weiterentwickelt wie zuvor.</p>
